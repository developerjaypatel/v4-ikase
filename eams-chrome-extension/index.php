<h1>EAMS Chrome Extension</h1>
Using EAMS Search/Import chrome extension you can search and import EAMS data using ADJ number. For more information please refer below guidelines.
<p>
Guideline - steps for installing EAMS chrome extension (Solution Installation):
</p>
<table>
	<tr>
		<td valign="top" width="50%">
			<ol>
				<li>Download the EAMS chrome extension. <a href="resources/extension/eams-import.zip">Click here to download EAMS chrome extension</a>.</li>
				<li>Extract the downloaded ZIP file of extension.</li>
				<li>Click on - Google Chrome Menu > Extensions > Manage Extensions. <a href="resources/image/ss1.png" target="_blank">Click here for screenshot.</a></li>
				<li>Enable "Developer mode" option showing in top right corner. <a href="resources/image/ss2.png" target="_blank">Click here for screenshot.</a></li>
				<li>Click on "Load unpacked" button. <a href="resources/image/ss3.png" target="_blank">Click here for screenshot.</a></li>
				<li>Select chrome extension folder.</li>
			</ol>
		</td>
		<td valign="top" width="50%">
			<video width="100%" controls>
			  <source src="resources/video/video1.mp4" type="video/mp4">
			Your browser does not support the video tag.
			</video>
		</td>
	</tr>
</table>
<p>
Guideline - steps for using EAMS chrome extension (EAMS Import):
</p>
<table>
<tr>
	<td valign="top" width="50%">
		<ol>
			<li>Click on - Kases > EAMS Import. <a href="resources/image/ss4.png" target="_blank">Click here for screenshot.</a></li>
			<li>Enter ADJ number. <a href="resources/image/ss5.png" target="_blank">Click here for screen shot.</a></li>
			<li>Click on - EAMS import chrome extension > Get ADJ Number. <a href="resources/image/ss6.png" target="_blank">Click here for screenshot.</a></li>
			<li>Click on - EAMS import chrome extension > Fill Requested Information (This option need to do first time or after doing this process before few minutes). <a href="resources/image/ss7.png" target="_blank">Click here for screenshot.</a></li>
			<li>Now click on "I'm not a robot" and Next button. <a href="resources/image/ss8.png" target="_blank">Click here for screenshot.</a></li>
			<li>Click on - EAMS import chrome extension > Search & Save ADJ Data. <a href="resources/image/ss9.png" target="_blank">Click here for screenshot.</a></li>
			<li>Now Case Detail Information/Case Events will show. <a href="resources/image/ss10.png" target="_blank">Click here for screen shot.</a></li>
			<li>Then click on - Import EAMS Data > Search button. <a href="resources/image/ss11.png" target="_blank">Click here for screenshot.</a></li>
			<li>Finally click on - Import EAMS Data > Import button. <a href="resources/image/ss12.png" target="_blank">Click here for screenshot.</a></li>
		</ol>
	</td>
	<td valign="top" width="50%">
